package com.Turpan.Java;

public class shorthandAndCompoundOperators {

    public static void main(String[] args) {


        int num1=13;
        int num2=8;

    //     num1=num1+num2  <=>  num1+=num2
         num1+=num2;


        System.out.println(num1);

        int a=17;
        int b=3;

      //  a=a-b;
        a-=b;

        System.out.println(a);

       int san1=5;
       int san2=4;

      // san1=san1*san2;
       san1*=san2;

        System.out.println(san1);

        int c=15;
        int d=3;

    //    c=c/d;
        c/=d;

        System.out.println(c);





    }
}
